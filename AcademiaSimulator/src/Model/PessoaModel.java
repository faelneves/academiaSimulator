package Model;

/**
 *
 * @author rafael
 */
public class PessoaModel {

    protected String nome, CPF, dtNasc, telefone;

    public PessoaModel(String nome, String CPF, String dtNasc, String telefone) {
        this.nome = nome;
        this.CPF = CPF;
        this.dtNasc = dtNasc;
        this.telefone = telefone;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public String getDtNasc() {
        return dtNasc;
    }

    public void setDtNasc(String dtNasc) {
        this.dtNasc = dtNasc;
    }

}
